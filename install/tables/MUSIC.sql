CREATE TABLE `MUSIC` (  
`ID` int(11) NOT NULL AUTO_INCREMENT, 
`USER_ID` int(11) DEFAULT NULL, 
`ID_DIR` int(11) DEFAULT '0',  
`FACT_NAME` varchar(300) DEFAULT NULL,  
`ARTIST` varchar(300) DEFAULT NULL,  
`NAME` varchar(300) DEFAULT NULL,  
`GENRE` varchar(300) DEFAULT NULL,  
`EXT` varchar(50) DEFAULT NULL,  
`SIZE` int(11) DEFAULT NULL,  
`TIME` int(11) DEFAULT NULL,  
`RATING` double(10,2) DEFAULT '0.00',  
`SHOW` int(11) DEFAULT '0',  
`DURATION` varchar(100) DEFAULT NULL,  
`BITRATE` int(11) DEFAULT '0',  
`ALBUM` varchar(300) DEFAULT NULL,  
`MESSAGE` varchar(5000) DEFAULT NULL,  
`PRIVATE_COMMENTS` int(11) DEFAULT NULL,
`ADULT` int(11) DEFAULT '0',
`SHIF` varchar(100) DEFAULT NULL,
`PATH` varchar(100) DEFAULT NULL,
PRIMARY KEY (`ID`)
) ENGINE=MyISAM  DEFAULT CHARSET=utf8mb4 AUTO_INCREMENT=1;